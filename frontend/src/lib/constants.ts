/**
 * Централизованные настройки frontend-клиента.
 *
 * Хранит параметры upload/download-процессов, polling-интервалы, timeout'ы и
 * TTL кэша thumbnail URL в одном месте, чтобы не размазывать числовые значения
 * по коду.
 */

// ── Uploads ──────────────────────────────────────────────────────────────────

/**
 * Размер одной части multipart upload.
 *
 * Значение равно 8 MB и соответствует backend-настройке по умолчанию.
 */
export const UPLOAD_PART_SIZE = 8 * 1024 * 1024;

/**
 * Максимальное количество upload'ов, выполняющихся одновременно end-to-end.
 *
 * Ограничение удерживает количество параллельных DB-транзакций и MinIO
 * thread-pool slots в безопасном диапазоне для нескольких uvicorn workers.
 * Также значение остаётся ниже backend-квоты активных upload-сессий
 * пользователя.
 */
export const MAX_CONCURRENT_UPLOADS = 5;

/**
 * Максимальное количество повторных попыток создания upload-сессии.
 *
 * Используется, когда создание сессии сталкивается с race condition: quota slot
 * уже освобождается, но backend ещё не успел обновить состояние квоты.
 */
export const UPLOAD_RETRY_MAX = 4;

/**
 * Базовая задержка retry при создании upload-сессии.
 *
 * Для попытки `N` задержка рассчитывается как:
 *
 * ```ts
 * (N + 1) * UPLOAD_RETRY_BASE_MS
 * ```
 */
export const UPLOAD_RETRY_BASE_MS = 1500;

// ── Archive downloads ────────────────────────────────────────────────────────

/**
 * Интервал polling при ожидании фоновой задачи создания архива.
 */
export const ARCHIVE_POLL_MS = 2000;

/**
 * Максимальное время ожидания создания архива.
 *
 * После истечения этого времени frontend прекращает polling.
 */
export const ARCHIVE_TIMEOUT_MS = 15 * 60 * 1000; // 15 minutes

// ── Thumbnails ───────────────────────────────────────────────────────────────

/**
 * TTL presigned thumbnail URL в `sessionStorage`.
 *
 * Значение должно быть меньше backend-срока жизни presigned URL
 * `PRESIGNED_DOWNLOAD_EXPIRE_SECONDS`, чтобы frontend не использовал
 * протухший URL. Иначе `<img>` может получить `403`, что приведёт к
 * ORB-блокировке изображения браузером.
 */
export const THUMBNAIL_URL_TTL_MS = 12 * 60 * 1000; // 12 minutes

/**
 * TTL для отрицательного результата thumbnail (превью пока нет).
 *
 * Намеренно короткий: для только что загруженного файла превью генерируется
 * на сервере асинхронно (особенно медленно для видео через ffmpeg). Длинный
 * кэш `null` заставил бы UI показывать иконку до 12 минут даже после того, как
 * превью уже готово. Короткий TTL позволяет быстро перепросить и показать его.
 */
export const THUMBNAIL_NEGATIVE_TTL_MS = 45 * 1000; // 45 seconds
